              MimeBodyPart fileattach4=new MimeBodyPart();
            fileattach4.attachFile("/C:/Users/Dell/Desktop/audit/day4.pdf");
         emailcontent.addBodyPart(fileattach);
         emailcontent.addBodyPart(fileattach2);
         emailcontent.addBodyPart(fileattach3);
