         message.setSubject(submail);
         message.setText(jTextArea1.getText());
         
         Multipart emailcontent= new MimeMultipart();
         MimeBodyPart fileattach=new MimeBodyPart();
         fileattach.attachFile("/C:/Users/Dell/Desktop/audit/day1.pdf");
            MimeBodyPart fileattach2=new MimeBodyPart();
